export function buildWebsiteLinks(details: MerchantSocialDetails | null | undefined) {
  return (details?.external_websites || []).filter(
    (site) => site?.name?.trim() && site?.url?.trim()
  );
}

export function hasMerchantSocialSidebarContent(details: MerchantSocialDetails | null | undefined) {
  return (
    buildFollowLinks(details).length > 0 ||
    buildConnectLinks(details).length > 0 ||
    buildWebsiteLinks(details).length > 0
  );
}
