export function buildFollowLinks(details: MerchantSocialDetails | null | undefined): SocialLinkDef[] {
  if (!details) return [];
  return FOLLOW_DEFS.filter((def) => {
    const url = details[def.key];
    return typeof url === "string" && url.trim();
  }).map((def) => ({
    key: def.key,
    label: def.label,
    url: String(details[def.key]).trim(),
    icon: def.icon,
    color: def.color,
  }));
}

